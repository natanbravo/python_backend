# Calculando o valor hora de um colaborador 


salario_mensal = input("Qual o valor do seu salário por mês ?")

horas_trabalhadas_por_mês = input("Quantas horas você trabalha por mês ?")

valor_hora = int(salario_mensal) / int(horas_trabalhadas_por_mês)


print(f'Você recebe {valor_hora} reais por hora trabalhada.')